export function getSquareInTable(square: string): {
  rowIdx: number;
  colIdx: number;
} {
  const file = square.charAt(0);
  const rank = parseInt(square.charAt(1), 10);

  const colIdx = file.charCodeAt(0) - "a".charCodeAt(0);
  const rowIdx = 8 - rank;

  return { rowIdx, colIdx };
}
